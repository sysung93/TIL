import java.util.Scanner;

public class exam06 {
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		System.out.print("김치찌개? ");
		int a = sc.nextInt();
		
		System.out.print("된장찌개? ");
		int b = sc.nextInt();
		
		System.out.print("제육볶음? ");
		int c = sc.nextInt();
		
		int payment = ((a * 5000) + (b * 4000) + (c * 6000))/5;
		
		System.out.println("한명당 지불해야 하는 금액 : " + payment);
		
	}

}
